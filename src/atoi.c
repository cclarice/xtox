#include "../xtox.h"

int	ternar_sgn(char a, char b, int c, int d)
{
	if (a == b)
		return (d);
	else
		return (c);
}

int	ft_atoi(char *str)
{
	const char	sgn = ternar_sgn(*str, '-', 1, -1);
	int			ret;

	ret = 0;
	if (*str == '+' || *str == '-')
		str++;
	while (*str == '0')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		ret *= 10;
		ret += *str - '0';
		if (ret & 0x80000000)
		{
			if (sgn == 1)
				return (0x7fffffff);
			else
				return (0x80000000);
		}
		str++;
	}
	return (ret * sgn);
}
