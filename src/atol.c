#include "../xtox.h"

long	ft_atol(char *str)
{
	const char	sgn = ternar_sgn(*str, '-', 1, -1);
	long		ret;

	ret = 0;
	if (*str == '+' || *str == '-')
		str++;
	while (*str == '0')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		ret *= 10;
		ret += *str - '0';
		if (ret & 0x8000000000000000)
		{
			if (sgn == 1)
				return (0x7fffffffffffffff);
			else
				return (0x8000000000000000);
		}
		str++;
	}
	return (ret * sgn);
}
