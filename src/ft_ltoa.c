#include "../xtox.h"

#include <stdio.h>

short	ft_longlen(long l)
{
	short	c;

	c = 1;
	if (l <= -1 && ++c)
		l *= -1;
	while (l >= 10 && ++c)
		l /= 10;
	return (c);
}

char	*ft_ltoa(long l)
{
	char		*ret;
	short		len;

	len = ft_longlen(l);
	ret = (char *)malloc(sizeof(char) * len + 1);
	if (ret)
		ret[len--] = 0;
	if (ret && l >= 0)
	{
		while (len >= 0)
		{
			ret[len--] = l % 10 + '0';
			l /= 10;
		}
	}
	else if (ret)
	{
		ret[0] = '-';
		while (len >= 1)
		{
			ret[len--] = (l % 10) * -1 + '0';
			l /= 10;
		}
	}
	return (ret);
}
